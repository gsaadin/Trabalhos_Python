# Primeiramente, é necessário atribuir certo valor de entrada, em alguma variável, nesse caso, ela será associada com a letra "a". Para isso será usado o comando imput(), junto ao comando int(), para poder mudar o tipo do objeto, (a priori string) nesse sentido, possibilitando a realização de operações matemáticas.
a= int(input())
# A mesma lógica será usada para atribuir valor a segunda variável, dessa vez com o nome associado "b".
b= int(input())
# Por fim, serão escritas as operações aritiméticas, que aparecerção na saída por meio do comando print(), depois dos valores escolhidos na entrada.
print("a =", a)
print("b =", b)
print("a + b =", a+b)
print("a - b =", a-b)
print("a * b =", a*b)
print("a // b =", a//b)
print("a % b =", a%b)
